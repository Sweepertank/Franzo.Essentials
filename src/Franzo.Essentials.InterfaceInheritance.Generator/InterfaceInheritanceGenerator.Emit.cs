    internal static void Emit(Context context)
    {
        var sb = new StringBuilder();
        var writer = new IndentedTextWriter(new StringWriter(sb), "    ");

        writer.WriteLine("// <auto-generated />");
        writer.WriteLine();
        writer.WriteLine("#nullable restore annotations");
        writer.WriteLine("#nullable disable warnings");
        writer.WriteLine("#nullable disable warnings");
        writer.WriteLine("#pragma warning disable CS0108");
        writer.WriteLine("#pragma warning disable CS0618");
        writer.WriteLine("#pragma warning disable CS8387");
        writer.WriteLine();

        foreach (var type in context.AnalysisData.RootTypesToEmit)
        {
            EmitTypeWithNamespaceDeclaration(type, writer, context);
        }

        context.SourceProductionContext.AddSource(
            "Franzo.Essentials.InterfaceInheritance.Generated.cs",
            sb.ToString());
    }

    internal static void EmitTypeWithNamespaceDeclaration(
        InternalTypeSymbol type,
        IndentedTextWriter writer,
        Context context)
    {
        if (!type.RoslynSymbol.ContainingNamespace.IsGlobalNamespace)
        {
            writer.Write("namespace ");
            writer.Write(type.RoslynSymbol.ContainingNamespace.ToDisplayString());
            writer.WriteLine();
            writer.WriteBracedSectionStart();
        }

        EmitType(type, writer, context);

        if (!type.RoslynSymbol.ContainingNamespace.IsGlobalNamespace)
        {
            writer.WriteBracedSectionEnd();
            writer.WriteLine();
        }
    }

    private static void EmitTypes(
        IEnumerable<InternalTypeSymbol> types,
        IndentedTextWriter writer,
        Context context)
    {
        foreach (var type in types)
        {
            EmitType(type, writer, context);
        }
    }

    private static void EmitType(
        InternalTypeSymbol type,
        IndentedTextWriter writer,
        Context context)
    {
        writer.Write("partial");

        if (type.RoslynSymbol.TypeKind is TypeKind.Interface)
        {
            writer.Write($" interface");
        }
        else
        {
            if (type.RoslynSymbol.IsRecord)
            {
                writer.Write(" record");
            }

            writer.Write(" ");
            writer.Write(type.RoslynSymbol.TypeKind is TypeKind.Class ? "class" : "struct");
        }

        writer.Write(" ");
        writer.Write(type.RoslynSymbol.Name);

        if (type.IsDataClass)
        {
            writer.Write(" : ");
            writer.Write(typeof(InterfaceData).GloballyQualifiedName());
            writer.Write("<");
            writer.Write(type.ContainingType!.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(">");
        }
        else if (type.RoslynSymbol.TypeKind.IsClassOrStruct())
        {
            if (type.AttributeSpecifiedDirectInterfaces.Count > 0)
            {
                writer.Write(" : ");
                foreach ((var i, var @interface) in type.AttributeSpecifiedDirectInterfaces.IndicesAndItems())
                {
                    writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
                    if (i != type.AttributeSpecifiedDirectInterfaces.Count - 1)
                    {
                        writer.Write(", ");
                    }
                }
            }
        }

        writer.WriteLine();
        writer.WriteBracedSectionStart();

        if (type.IsDataClass)
        {
            writer.Write("public static readonly global::System.Reflection.ConstructorInfo ");
            writer.Write(GeneratedConstructorPropertyName);
            writer.Write(" = typeof(");
            writer.Write(type.RoslynSymbol.ToDisplayString());
            writer.Write(").GetConstructors()[0];");
            writer.WriteLine();
        }

        if (type.RoslynSymbol.TypeKind.IsClassOrStruct())
        {
            if (!type.IsDataClass)
            {
                EmitRealDataFieldsAndDataPropertyImplementations(type, writer, context);
            }

            EmitFakeDataFieldsAndConstructors(type, writer, context);
        }

        if (type.DataClass is not null)
        {
            foreach (var property in type.DataClass.DeclaredProperties)
            {
                EmitDataClassPropertyProxy(
                    property.RoslynSymbol,
                    type.RoslynSymbol,
                    writer,
                    context);
            }

            foreach (var @event in type.DataClass.DeclaredEvents)
            {
                EmitDataClassEventProxy(
                    @event.RoslynSymbol,
                    type.RoslynSymbol,
                    writer,
                    context);
            }

            writer.Write("public ");
            writer.Write(type.DataClass.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(" ");
            writer.Write(GeneratedInterfaceDataPropertyName);
            writer.Write(" { get; }");
            writer.WriteLine();
        }

        foreach (var @interface in type.InterfacesInheriting)
        {
            foreach (var feature in @interface.DeclaredFeatures)
            {
                if (!(feature.RoslynSymbol.IsAccessibleFromType(
                        type.RoslynSymbol,
                        context.Compilation)
                     || feature.RoslynSymbol.DeclaredAccessibility is Accessibility.Protected
                     || (feature.RoslynSymbol.DeclaredAccessibility is Accessibility.ProtectedAndInternal
                          && feature.RoslynSymbol.ContainingAssembly.GivesAccessTo(
                            type.RoslynSymbol.ContainingAssembly)))
                    || feature.RoslynSymbol is IMethodSymbol
                    {
                        MethodKind: MethodKind.PropertyGet
                            or MethodKind.PropertySet
                            or MethodKind.EventAdd
                            or MethodKind.EventRemove
                    }
                    || feature.RoslynSymbol is IFieldSymbol
                    {
                        IsConst: false,
                        IsReadOnly: false
                    }
                    // @todo: once partial properties are in,
                    // we should change this so that we *do* emit inheritances for public abstract properties and methods,
                    // but just mark them them as partial
                    // (ideally we would do so for events too, but it doesn't look like partial events are coming any time soon)
                    || (feature.RoslynSymbol.DeclaredAccessibility is Accessibility.Public
                        && feature.RoslynSymbol.IsAbstract)
                    || feature.RoslynSymbol.Name == type.RoslynSymbol.Name)
                {
                    continue;
                }

                foreach (var shadowingType in feature.TypesDeclaringShadowingFeatures)
                {
                    if (type.RoslynSymbol.IsAssignableTo(
                            shadowingType.RoslynSymbol,
                            context.Compilation)
                        || type.AttributeSpecifiedDirectInterfaces.Any(
                            i => i.RoslynSymbol.IsAssignableTo(
                                shadowingType.RoslynSymbol,
                                context.Compilation)))
                    {
                        continue;
                    }
                }

                EmitFeatureInheritance(
                    feature,
                    @interface.RoslynSymbol,
                    type.RoslynSymbol,
                    writer,
                    context);
            }
        }

        foreach (var @interface in type.DirectInterfaces)
        {
            EmitAsInterfaceMethod(@interface, writer, context);
        }

        foreach (var memberType in type.DeclaredTypes)
        {
            EmitType(memberType, writer, context);
        }

        writer.WriteBracedSectionEnd();
    }

    private static void EmitRealDataFieldsAndDataPropertyImplementations(
        InternalTypeSymbol type,
        IndentedTextWriter writer,
        Context context)
    {
        var x = type.InterfacesInheriting.ToArray();
        foreach (var @interface in type.InterfacesInheriting)
        {
            if (@interface.DataClass is null) continue;

            writer.Write("private ");
            writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(".");
            writer.Write(InterfaceDataClassName);
            writer.Write(" ");
            EmitRealDataFieldName(@interface, writer);
            writer.WriteLine();

            writer.Indent++;
            writer.Write("= (");
            writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(".");
            writer.Write(InterfaceDataClassName);
            writer.Write(")global::System.Runtime.CompilerServices.RuntimeHelpers.GetUninitializedObject(typeof(");
            writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(".");
            writer.Write(InterfaceDataClassName);
            writer.Write("));");
            writer.WriteLine();
            writer.Indent--;
        }

        foreach (var @interface in type.InterfacesInheriting)
        {
            if (@interface.DataClass is null) continue;

            writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(".");
            writer.Write(InterfaceDataClassName);
            writer.Write(" ");
            writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(".");
            writer.Write(GeneratedInterfaceDataPropertyName);
            writer.WriteLine();

            writer.WriteBracedSectionStart();
            writer.Write("get => ");
            EmitRealDataFieldName(@interface, writer);
            writer.Write(";");
            writer.WriteLine();
            writer.WriteBracedSectionEnd();
        }
    }

    private static void EmitAsInterfaceMethod(
        InternalTypeSymbol @interface,
        IndentedTextWriter writer,
        Context context)
    {
        writer.Write("private ");
        writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
        writer.Write(" As");
        writer.Write(@interface.RoslynSymbol.Name.WithoutInterfaceI());
        writer.Write("()");
        writer.WriteLine();
        writer.WriteBracedSectionStart();
