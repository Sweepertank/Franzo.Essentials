    private static void EmitMainModifiers(
        ISymbol symbol,
        IndentedTextWriter writer,
        bool includeSealed = false)
    {
        EmitAccessibilityModifiers(symbol.DeclaredAccessibility, writer);

        if (includeSealed)
        {
            writer.Write(" sealed");
        }

        if (symbol.IsStatic && symbol is not IFieldSymbol { IsConst: true })
        {
            writer.Write(" static");
        }
    }

    private static void EmitAccessibilityModifiers(
        Accessibility accessibility,
        IndentedTextWriter writer)
    {
        if (accessibility is Accessibility.Public)
        {
            writer.Write("public");
        }
        else if (accessibility is Accessibility.Protected)
        {
            writer.Write("protected");
        }
        else if (accessibility is Accessibility.Internal)
        {
            writer.Write("internal");
        }
        else if (accessibility is Accessibility.Private)
        {
            writer.Write("private");
        }
        else if (accessibility is Accessibility.ProtectedOrInternal)
        {
            writer.Write("protected internal");
        }
        else if (accessibility is Accessibility.ProtectedAndInternal)
        {
            writer.Write("private protected");
        }
    }

    private static void EmitAttributes(
        ISymbol symbol,
        INamedTypeSymbol withinType,
        IndentedTextWriter writer,
        Context context,
        bool separateLines)
    {
        var attributes = symbol.GetAttributes();
        // Note: protected/private protected member types in a interface CAN currently be accessed by implementing classes
        foreach ((var i, var attribute) in attributes.IndicesAndItems())
        {
            if (attribute.AttributeClass is null
                || attribute.AttributeClass.IsCompilerGeneratedAttributeType()
                || !attribute.AttributeClass.IsAccessibleFromType(withinType, context.Compilation)
                || attribute.AttributeClass.TypeArguments.Any(
                    a => !a.IsAccessibleFromType(withinType, context.Compilation))
                || attribute.ConstructorArguments
                    .Concat(attribute.NamedArguments.Select(p => p.Value))
                    .Any(arg => arg.Kind is TypedConstantKind.Error)
                || attribute.AttributeConstructor is null
                || !attribute.AttributeConstructor.IsAccessibleFromType(withinType, context.Compilation))
            {
                continue;
            }

            EmitAttribute(attribute, writer);

            if (separateLines)
            {
                writer.WriteLine();
            }
            else
            {
                if (i != attributes.Length - 1)
                {
                    writer.Write(" ");
                }
            }
        }
    }

    private static void EmitAttribute(AttributeData attribute, IndentedTextWriter writer)
    {
        writer.Write("[");
        writer.Write(attribute.AttributeClass!.ToFullyQualifiedDisplayString());
        writer.Write("(");

        foreach ((var i, var argument) in attribute.ConstructorArguments.IndicesAndItems())
        {
            EmitTypedConstant(argument, writer);
            if (i != attribute.ConstructorArguments.Length - 1
                || attribute.NamedArguments.Length > 0)
            {
                writer.Write(", ");
            }
        }

        foreach ((var i, var pair) in attribute.NamedArguments.IndicesAndItems())
        {
            writer.Write(pair.Key);
            writer.Write(" = ");
            EmitTypedConstant(pair.Value, writer);
            if (i != attribute.NamedArguments.Length - 1)
            {
                writer.Write(", ");
            }
        }

        writer.Write(")]");
    }

    private static void EmitTypedConstant(TypedConstant typedConstant, IndentedTextWriter writer)
    {
        Debug.Assert(typedConstant.Kind is not TypedConstantKind.Error && typedConstant.Type is not null);

        if (typedConstant.IsNull)
        {
            EmitNull(writer);
        }
        else
        {
            switch (typedConstant.Kind)
            {
                case TypedConstantKind.Array:
                    EmitArrayTypedConstant(typedConstant, writer);
                    break;
                case TypedConstantKind.Primitive or TypedConstantKind.Enum:
                    EmitConstant(typedConstant.Value, typedConstant.Type!, writer);
                    break;
                case TypedConstantKind.Type:
                    EmitTypeTypedConstant(typedConstant, writer);
                    break;
            };
        }
    }

    private static void EmitArrayTypedConstant(TypedConstant self, IndentedTextWriter writer)
    {
        Debug.Assert(self.Type is not null);

        var arrayType = (IArrayTypeSymbol)self.Type!;
        writer.Write("new ");
        writer.Write(arrayType.ElementType.ToFullyQualifiedDisplayString());
        writer.Write("[] { ");
        EmitCommaSeparatedTypedConstants(self.Values, writer);
        writer.Write(" }");
    }

    private static void EmitCommaSeparatedTypedConstants(
        ImmutableArray<TypedConstant> typedConstants,
        IndentedTextWriter writer)
    {
        foreach ((var i, var typedConstant) in typedConstants.IndicesAndItems())
        {
            EmitTypedConstant(typedConstant, writer);
            if (i != typedConstants.Length - 1)
            {
                writer.Write(", ");
            }
        }
    }

    private static void EmitConstant(
        object? constant,
        ITypeSymbol constantType,
        IndentedTextWriter writer)
    {
        if (constant is null)
        {
            EmitNull(writer);
        }
        else if (constantType.TypeKind is TypeKind.Enum)
        {
            EmitEnumValueFromConstant(constant, (INamedTypeSymbol)constantType, writer);
        }
        else if (constant is char c)
        {
            writer.Write("'");
            writer.Write(c);
            writer.Write("'");
        }
        else if (constant is string str)
        {
            writer.Write(SyntaxFactory.Literal(str).ToString());
        }
        else if (constant is bool b)
        {
            writer.Write(b ? "true" : "false");
        }
        else
        {
            writer.Write(constant);
        }
    }

    private static void EmitEnumValueFromConstant(
        object constant,
        INamedTypeSymbol enumType,
        IndentedTextWriter writer)
    {
        IFieldSymbol matchingField = null!;
        foreach (var member in enumType.GetMembers())
        {
            var field = (IFieldSymbol)member;
            if (object.Equals(field.ConstantValue, constant))
            {
                matchingField = field;
                break;
            }
        }

        writer.Write(enumType.ToFullyQualifiedDisplayString());
        writer.Write(".");
        writer.Write(matchingField.Name);
    }

    private static void EmitTypeTypedConstant(
        TypedConstant typedConstant,
        IndentedTextWriter writer)
    {
        var type = (INamedTypeSymbol)typedConstant.Value!;
        writer.Write("typeof(");
        writer.Write(type.ToFullyQualifiedDisplayString());
        writer.Write(")");
    }

    private static void EmitNull(IndentedTextWriter writer)
