    private static void EmitMainModifiers(
        ISymbol symbol,
        TypeEmissionContext cxt,
        bool includeSealed = false)
    {
        EmitAccessibilityModifiers(symbol.DeclaredAccessibility, cxt);

        if (includeSealed)
        {
            cxt.Writer.Write(" sealed");
        }

        if (symbol.IsStatic && symbol is not IFieldSymbol { IsConst: true })
        {
            cxt.Writer.Write(" static");
        }
    }

    private static void EmitAccessibilityModifiers(
        Accessibility accessibility,
        TypeEmissionContext cxt)
    {
        if (accessibility is Accessibility.Public)
        {
            cxt.Writer.Write("public");
        }
        else if (accessibility is Accessibility.Protected)
        {
            cxt.Writer.Write("protected");
        }
        else if (accessibility is Accessibility.Internal)
        {
            cxt.Writer.Write("internal");
        }
        else if (accessibility is Accessibility.Private)
        {
            cxt.Writer.Write("private");
        }
        else if (accessibility is Accessibility.ProtectedOrInternal)
        {
            cxt.Writer.Write("protected internal");
        }
        else if (accessibility is Accessibility.ProtectedAndInternal)
        {
            cxt.Writer.Write("private protected");
        }
    }

    private static void EmitAttributes(
        ISymbol symbol,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt,
        bool separateLines)
    {
        var attributes = symbol.GetAttributes();
        // Note: protected/private protected member types in a interface CAN currently be accessed by implementing classes
        foreach ((var attribute, var last) in attributes.WithLastFlag())
        {
            if (attribute.AttributeClass is null
                || attribute.AttributeClass.IsCompilerGeneratedAttributeType()
                || !attribute.AttributeClass.IsAccessibleFromType(
                    withinType,
                    cxt.MainContext.Compilation)
                || attribute.AttributeClass.TypeArguments.Any(
                    a => !a.IsAccessibleFromType(withinType, cxt.MainContext.Compilation))
                || attribute.ConstructorArguments
                    .Concat(attribute.NamedArguments.Select(p => p.Value))
                    .Any(arg => arg.Kind is TypedConstantKind.Error)
                || attribute.AttributeConstructor is null
                || !attribute.AttributeConstructor.IsAccessibleFromType(
                    withinType,
                    cxt.MainContext.Compilation))
            {
                continue;
            }

            EmitAttribute(attribute, cxt);

            if (separateLines)
            {
                cxt.Writer.WriteLine();
            }
            else
            {
                if (!last)
                {
                    cxt.Writer.Write(" ");
                }
            }
        }
    }

    private static void EmitAttribute(AttributeData attribute, TypeEmissionContext cxt)
    {
        cxt.Writer.Write("[");
        cxt.Writer.Write(attribute.AttributeClass!.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write("(");

        foreach ((var argument, var last) in attribute.ConstructorArguments.WithLastFlag())
        {
            EmitTypedConstant(argument, cxt);
            if (!last || attribute.NamedArguments.Length > 0)
            {
                cxt.Writer.Write(", ");
            }
        }

        foreach ((var pair, var last) in attribute.NamedArguments.WithLastFlag())
        {
            cxt.Writer.Write(pair.Key);
            cxt.Writer.Write(" = ");
            EmitTypedConstant(pair.Value, cxt);
            if (!last)
            {
                cxt.Writer.Write(", ");
            }
        }

        cxt.Writer.Write(")]");
    }

    private static void EmitTypedConstant(TypedConstant typedConstant, TypeEmissionContext cxt)
    {
        Debug.Assert(typedConstant.Kind is not TypedConstantKind.Error && typedConstant.Type is not null);

        if (typedConstant.IsNull)
        {
            EmitNull(cxt);
        }
        else
        {
            switch (typedConstant.Kind)
            {
                case TypedConstantKind.Array:
                    EmitArrayTypedConstant(typedConstant, cxt);
                    break;
                case TypedConstantKind.Primitive or TypedConstantKind.Enum:
                    EmitConstant(typedConstant.Value, typedConstant.Type!, cxt);
                    break;
                case TypedConstantKind.Type:
                    EmitTypeTypedConstant(typedConstant, cxt);
                    break;
            };
        }
    }

    private static void EmitArrayTypedConstant(TypedConstant self, TypeEmissionContext cxt)
    {
        Debug.Assert(self.Type is not null);

        var arrayType = (IArrayTypeSymbol)self.Type!;
        cxt.Writer.Write("new ");
        cxt.Writer.Write(arrayType.ElementType.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write("[] { ");
        EmitCommaSeparatedTypedConstants(self.Values, cxt);
        cxt.Writer.Write(" }");
    }

    private static void EmitCommaSeparatedTypedConstants(
        ImmutableArray<TypedConstant> typedConstants,
        TypeEmissionContext cxt)
    {
        foreach ((var typedConstant, var last) in typedConstants.WithLastFlag())
        {
            EmitTypedConstant(typedConstant, cxt);
            if (!last)
            {
                cxt.Writer.Write(", ");
            }
        }
    }

    private static void EmitConstant(
        object? constant,
        ITypeSymbol constantType,
        TypeEmissionContext cxt)
    {
        if (constant is null)
        {
            EmitNull(cxt);
        }
        else if (constantType.TypeKind is TypeKind.Enum)
        {
            EmitEnumValueFromConstant(constant, (INamedTypeSymbol)constantType, cxt);
        }
        else if (constant is char c)
        {
            cxt.Writer.Write("'");
            cxt.Writer.Write(c);
            cxt.Writer.Write("'");
        }
        else if (constant is string str)
        {
            cxt.Writer.Write(SyntaxFactory.Literal(str).ToString());
        }
        else if (constant is bool b)
        {
            cxt.Writer.Write(b ? "true" : "false");
        }
        else
        {
            cxt.Writer.Write(constant);
        }
    }

    private static void EmitEnumValueFromConstant(
        object constant,
        INamedTypeSymbol enumType,
        TypeEmissionContext cxt)
    {
        IFieldSymbol matchingField = null!;
        foreach (var member in enumType.GetMembers())
        {
            var field = (IFieldSymbol)member;
            if (object.Equals(field.ConstantValue, constant))
            {
                matchingField = field;
                break;
            }
        }

        cxt.Writer.Write(enumType.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write(".");
        cxt.Writer.Write(matchingField.Name);
    }

    private static void EmitTypeTypedConstant(
        TypedConstant typedConstant,
        TypeEmissionContext cxt)
    {
        var type = (INamedTypeSymbol)typedConstant.Value!;
        cxt.Writer.Write("typeof(");
        cxt.Writer.Write(type.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write(")");
    }

    private static void EmitNull(TypeEmissionContext cxt)
    {
        cxt.Writer.Write("null");
    }

    private static void EmitMethodParameterList(
        IMethodSymbol method,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt,
        bool isForUnsafeAccessor = false)
    {
        cxt.Writer.Write("(");
        EmitUnparenthesizedParameterList(
            method.Parameters,
            withinType,
            cxt,
            isForUnsafeAccessor);
        cxt.Writer.Write(")");
    }

    private static void EmitUnparenthesizedParameterList(
        ImmutableArray<IParameterSymbol> parameters,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt,
        bool isForUnsafeAccessor = false)
    {
        foreach (((var i, var parameter), var last) in parameters.IndicesAndItems().WithLastFlag())
        {
            if (!isForUnsafeAccessor)
            {
                EmitAttributes(parameter, withinType, cxt, false);
            }

            var refKindModifier = parameter.RefKind.ToCSharpString();
            if (refKindModifier is not null)
            {
                cxt.Writer.Write(refKindModifier);
                cxt.Writer.Write(" ");
            }

            cxt.Writer.Write(parameter.Type.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(" ");

            if (isForUnsafeAccessor)
            {
                cxt.Writer.Write("a");
                cxt.Writer.Write(i);
            }
            else
            {
                parameter.WriteVerbatimizedName(cxt.Writer);
            }

            if (parameter.HasExplicitDefaultValue && !isForUnsafeAccessor)
            {
                cxt.Writer.Write(" = ");
                EmitConstant(parameter.ExplicitDefaultValue, parameter.Type, cxt);
            }

            if (!last)
            {
                cxt.Writer.Write(", ");
            }
        }
    }

