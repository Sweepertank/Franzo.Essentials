    internal static void Emit(Context cxt)
    {
        var lk = new object();
        var count = 0;

        Parallel.ForEach(
            cxt.AnalysisData.RootTypesToEmit,
            (type, _, _) =>
            {
                var sb = new StringBuilder();
                var emissionCxt = new TypeEmissionContext(
                    cxt,
                    new IndentedTextWriter(new StringWriter(sb), "    "));

                emissionCxt.Writer.WriteLine("// <auto-generated />");
                emissionCxt.Writer.WriteLine();
                emissionCxt.Writer.WriteLine("#nullable restore annotations");
                emissionCxt.Writer.WriteLine("#nullable disable warnings");
                emissionCxt.Writer.WriteLine("#nullable disable warnings");
                emissionCxt.Writer.WriteLine("#pragma warning disable CS0108");
                emissionCxt.Writer.WriteLine("#pragma warning disable CS0618");
                emissionCxt.Writer.WriteLine("#pragma warning disable CS8387");
                emissionCxt.Writer.WriteLine("#pragma warning disable CS0114");
                emissionCxt.Writer.WriteLine("#pragma warning disable CS0693");
                emissionCxt.Writer.WriteLine();

                EmitTypeWithNamespaceDeclaration(type, emissionCxt);

                var s = sb.ToString();

                lock (lk)
                {
                    cxt.SourceProductionContext.AddSource(
                        $"{type.RoslynSymbol.ToNamespaceQualifiedFileSystemDisplayString()}.cs",
                        s);
                    count++;
                }
            });
    }

    private static void EmitTypeWithNamespaceDeclaration(
        InternalTypeSymbol type,
        TypeEmissionContext cxt)
    {
        if (!type.RoslynSymbol.ContainingNamespace.IsGlobalNamespace)
        {
            cxt.Writer.Write("namespace ");
            cxt.Writer.Write(type.RoslynSymbol.ContainingNamespace.ToDisplayString());
            cxt.Writer.WriteLine();
            cxt.Writer.WriteBracedSectionStart();
        }

        EmitType(type, cxt);

        if (!type.RoslynSymbol.ContainingNamespace.IsGlobalNamespace)
        {
            cxt.Writer.WriteBracedSectionEnd();
        }
    }

    private static void EmitTypes(
        IEnumerable<InternalTypeSymbol> types,
        TypeEmissionContext cxt)
    {
        foreach (var type in types)
        {
            EmitType(type, cxt);
        }
    }

    private static void OpenType(
        InternalTypeSymbol type,
        TypeEmissionContext cxt)
    {
        cxt.Writer.Write("partial");

        if (type.RoslynSymbol.TypeKind is TypeKind.Interface)
        {
            cxt.Writer.Write($" interface");
        }
        else
        {
            if (type.RoslynSymbol.IsRecord)
            {
                cxt.Writer.Write(" record");
            }

            cxt.Writer.Write(" ");
            cxt.Writer.Write(type.RoslynSymbol.TypeKind is TypeKind.Class ? "class" : "struct");
        }

        cxt.Writer.Write(" ");
        cxt.Writer.Write(type.RoslynSymbol.ToSimpleDisplayString());

        if (type.IsDataClass)
        {
            cxt.Writer.Write(" : ");
            cxt.Writer.Write(typeof(InterfaceData).GloballyQualifiedName());
            cxt.Writer.Write("<");
            cxt.Writer.Write(type.ContainingType!.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(">");
        }
        /*else if (type.RoslynSymbol.TypeKind.IsClassOrStruct())
        {
            if (type.AttributeSpecifiedDirectInterfaces.Count > 0)
            {
                cxt.Writer.Write(" : ");
                foreach ((var i, var @interface) in type.AttributeSpecifiedDirectInterfaces.IndicesAndItems())
                {
                    cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
                    if (i != type.AttributeSpecifiedDirectInterfaces.Count - 1)
                    {
                        cxt.Writer.Write(", ");
                    }
                }
            }
        }*/

        cxt.Writer.WriteLine();
        cxt.Writer.WriteBracedSectionStart();
    }

    private static void CloseType(TypeEmissionContext cxt)
    {
        cxt.Writer.WriteBracedSectionEnd();
    }

    private static void EmitType(
        InternalTypeSymbol type,
        TypeEmissionContext cxt)
    {
        OpenType(type, cxt);
        cxt.TypeStack.Push(type);

        if (type.IsDataClass)
        {
            cxt.Writer.Write("public static readonly global::System.Reflection.ConstructorInfo ");
            cxt.Writer.Write(GeneratedConstructorPropertyName);
            cxt.Writer.Write(" = typeof(");
            cxt.Writer.Write(type.RoslynSymbol.ToDisplayString());
            cxt.Writer.Write(").GetConstructors()[0];");
            cxt.Writer.WriteLine();

            foreach (var @event in type.DeclaredEvents)
            {
                EmitDataClassEventInvokeMethod(@event, type, cxt);
            }
        }

        if (type.RoslynSymbol.TypeKind.IsClassOrStruct())
        {
            if (!type.IsDataClass)
            {
                EmitRealDataFieldsAndDataPropertyImplementations(type, cxt);
            }

            EmitFakeDataFieldsAndConstructors(type, cxt);
        }

        if (type.DataClass is not null)
        {
            foreach (var property in type.DataClass.DeclaredProperties)
            {
                EmitDataClassPropertyProxy(
                    property.RoslynSymbol,
                    type.RoslynSymbol,
                    cxt);
            }

            foreach (var @event in type.DataClass.DeclaredEvents)
            {
                EmitDataClassEventProxy(
                    @event.RoslynSymbol,
                    type.RoslynSymbol,
                    cxt);
            }

            cxt.Writer.Write("public ");
            cxt.Writer.Write(type.DataClass.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(" ");
            cxt.Writer.Write(GeneratedInterfaceDataPropertyName);
            cxt.Writer.Write(" { get; }");
            cxt.Writer.WriteLine();
        }

        if (type.RoslynSymbol.TypeKind.IsClassOrStruct()
            && !type.DoNotGenerateInheritances)
        {
            foreach (var @interface in type.Interfaces)
            {
                foreach (var feature in @interface.DeclaredFeatures)
                {
                    if (!(feature.RoslynSymbol.IsAccessibleFromType(
                            type.RoslynSymbol,
                            cxt.MainContext.Compilation)
                        || feature.RoslynSymbol.DeclaredAccessibility is Accessibility.Protected
                        || (feature.RoslynSymbol.DeclaredAccessibility is Accessibility.ProtectedAndInternal
                            && feature.RoslynSymbol.ContainingAssembly.GivesAccessTo(
                                    type.RoslynSymbol.ContainingAssembly)))
                        || feature.RoslynSymbol is IMethodSymbol
                        {
                            MethodKind: MethodKind.PropertyGet
                                or MethodKind.PropertySet
                                or MethodKind.EventAdd
                                or MethodKind.EventRemove
                        }
                        || feature.RoslynSymbol is IFieldSymbol
                        {
                            IsConst: false,
                            IsReadOnly: false
                        }
                        || feature.RoslynSymbol.Name == type.RoslynSymbol.Name
                        || (feature.RoslynSymbol.IsVirtual && feature.RoslynSymbol.IsStatic))
                    {
                        continue;
                    }

                    // @todo: once partial properties are in,
                    // we should change this so that we *do* emit inheritances for public abstract properties and methods,
                    // but just mark them them as partial
                    // (ideally we would do so for events too, but it doesn't look like partial events are coming any time soon)
                    // note: above comment is old and probably confusing
                    var featureImpl = type.RoslynSymbol.FindImplementationForInterfaceMember(feature.RoslynSymbol);
                    if (feature.RoslynSymbol.DeclaredAccessibility is Accessibility.Public
                        && feature.RoslynSymbol.IsAbstract
                        && type.DirectInterfaces.All(
                            i => i.RoslynSymbol.FindImplementationForInterfaceMember(feature.RoslynSymbol) is null)
                        && (featureImpl is null || !featureImpl.IsExplicitImplementationFor(feature.RoslynSymbol)))
                    {
                        continue;
                    }

                    // @perf: checking directinterfaces is redundant here
                    // now that classes specify their interface lists before generation
                    // other places probably have similar redundancies too
                    if (feature.TypesDeclaringShadowingFeatures.Any(
                        shadowingType => type.RoslynSymbol.IsAssignableTo(
                                shadowingType.RoslynSymbol,
                                cxt.MainContext.Compilation)
                            || type.DirectInterfaces.Any(
                                i => i.RoslynSymbol.IsAssignableTo(
                                    shadowingType.RoslynSymbol,
                                    cxt.MainContext.Compilation))))
                    {
                        continue;
                    }

                    EmitFeatureInheritance(
                        feature,
                        @interface.RoslynSymbol,
                        type.RoslynSymbol,
                        cxt);
                }
            }
        }

        foreach (var @interface in type.DirectInterfaces)
        {
            EmitAsInterfaceMethod(@interface, cxt);
        }

        if (type.EmitDefaultConstructor)
        {
            cxt.Writer.Write("public ");
            cxt.Writer.Write(type.RoslynSymbol.Name);
            cxt.Writer.Write("()");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            foreach (var @interface in type.AnchorType.DirectInterfaces)
            {
                if (@interface.DataClass is null) continue;

                EmitFakeDataFieldName(@interface, cxt);
                cxt.Writer.Write(" = ");
                EmitConstructMethodName(@interface, cxt);
                cxt.Writer.Write("();");
                cxt.Writer.WriteLine();
            }

            cxt.Writer.WriteBracedSectionEnd();
        }

        foreach (var memberType in type.DeclaredTypes)
        {
            EmitType(memberType, cxt);
        }

        CloseType(cxt);
        cxt.TypeStack.Pop();
    }

    private static void EmitDataClassEventInvokeMethod(
        InternalEventSymbol @event,
        InternalTypeSymbol type,
        TypeEmissionContext cxt)
    {
        EmitEventInvokeMethod(
            @event.RoslynSymbol,
            type.RoslynSymbol,
            cxt,
            isProxy: false);
    }

    private static void EmitRealDataFieldsAndDataPropertyImplementations(
        InternalTypeSymbol type,
        TypeEmissionContext cxt)
    {
        foreach (var @interface in type.Interfaces)
        {
            if (@interface.DataClass is null) continue;

            cxt.Writer.Write("private ");
            cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(".");
            cxt.Writer.Write(InterfaceDataClassName);
            cxt.Writer.Write(" ");
            EmitRealDataFieldName(@interface, cxt);
            cxt.Writer.WriteLine();

            cxt.Writer.Indent++;
            cxt.Writer.Write("= (");
            cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(".");
            cxt.Writer.Write(InterfaceDataClassName);
            cxt.Writer.Write(")global::System.Runtime.CompilerServices.RuntimeHelpers.GetUninitializedObject(typeof(");
            cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(".");
            cxt.Writer.Write(InterfaceDataClassName);
            cxt.Writer.Write("));");
            cxt.Writer.WriteLine();
            cxt.Writer.Indent--;
        }

        foreach (var @interface in type.Interfaces)
        {
            if (@interface.DataClass is null) continue;

            cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(".");
            cxt.Writer.Write(InterfaceDataClassName);
            cxt.Writer.Write(" ");
            cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(".");
            cxt.Writer.Write(GeneratedInterfaceDataPropertyName);
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();
            cxt.Writer.Write("get => ");
            EmitRealDataFieldName(@interface, cxt);
            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();
            cxt.Writer.WriteBracedSectionEnd();
        }
    }

    private static void EmitAsInterfaceMethod(
        InternalTypeSymbol @interface,
        TypeEmissionContext cxt)
    {
        cxt.Writer.Write("private ");
        cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write(" As");
        cxt.Writer.Write(@interface.RoslynSymbol.MemberifiedName());
        cxt.Writer.Write("()");
        cxt.Writer.WriteLine();
        cxt.Writer.WriteBracedSectionStart();
        cxt.Writer.WriteLine("return this;");
        cxt.Writer.WriteBracedSectionEnd();
    }

    private static void EmitFakeDataFieldsAndConstructors(
        InternalTypeSymbol type,
        TypeEmissionContext cxt)
    {
        foreach (var @interface in type.AnchorType.DirectInterfaces)
        {
            if (@interface.DataClass is null) continue;

            cxt.Writer.Write("private ");
            cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(".");
            cxt.Writer.Write(InterfaceDataClassName);
            cxt.Writer.Write(" ");
            EmitFakeDataFieldName(@interface, cxt);
            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();
        }

        foreach (var @interface in type.AnchorType.DirectInterfaces)
        {
            if (@interface.DataClass is null) continue;

            cxt.Writer.Write("private ");
            cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(".");
            cxt.Writer.Write(InterfaceDataClassName);
            cxt.Writer.Write(" ");
            EmitConstructMethodName(@interface, cxt);
            EmitMethodParameterList(
                @interface.DataClass.ConstructorIfDataClass!.RoslynSymbol,
                type.RoslynSymbol,
                cxt);
            cxt.Writer.WriteLine();
            cxt.Writer.WriteBracedSectionStart();

            cxt.Writer.Write("if (");
            if (type.IsDataClass)
            {
                cxt.Writer.Write("((");
                cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
                cxt.Writer.Write(")");
                cxt.Writer.Write(nameof(InterfaceData<object>.This));
                cxt.Writer.Write(").");
                cxt.Writer.Write(GeneratedInterfaceDataPropertyName);
            }
            else
            {
                EmitRealDataFieldName(@interface, cxt);
            }
            cxt.Writer.Write(".");
            cxt.Writer.Write(nameof(InterfaceData.__IsConstructed));
            cxt.Writer.Write(") return null!;");
            cxt.Writer.WriteLine();

            if (!type.IsDataClass)
            {
                foreach (var ancestorInterface in @interface.SelfAndInterfaces)
                {
                    if (ancestorInterface.DataClass is null) continue;

                    EmitRealDataFieldName(ancestorInterface, cxt);
                    cxt.Writer.Write(".");
                    cxt.Writer.Write(nameof(InterfaceData<object>.__This));
                    cxt.Writer.Write(" = this;");
                    cxt.Writer.WriteLine();
                }
            }

            // @todo: remove string interpolations if needed for perf

            // @todo: create delegate and invoke that instead
            cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(".");
            cxt.Writer.Write(InterfaceDataClassName);
            cxt.Writer.Write(".");
            cxt.Writer.Write(GeneratedConstructorPropertyName);
            cxt.Writer.Write(".Invoke(");
            if (type.IsDataClass)
            {
                cxt.Writer.Write("((");
                cxt.Writer.Write(@interface.RoslynSymbol.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
                cxt.Writer.Write(")");
                cxt.Writer.Write(nameof(InterfaceData<object>.This));
                cxt.Writer.Write(").");
                cxt.Writer.Write(GeneratedInterfaceDataPropertyName);
            }
            else
            {
                EmitRealDataFieldName(@interface, cxt);
            }
            cxt.Writer.Write(", new object?[] { ");
            EmitUnparenthesizedArgumentListFromParameters(
                @interface.DataClass.ConstructorIfDataClass.RoslynSymbol.Parameters,
                cxt);
            cxt.Writer.Write(" });");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteLine($"return null!;");

            cxt.Writer.WriteBracedSectionEnd();
        }
    }

