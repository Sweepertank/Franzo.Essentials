    }

    private static void EmitFakeDataFieldsAndConstructors(
        InternalTypeSymbol type,
        IndentedTextWriter writer,
        Context context)
    {
        var anchorType = type;
        if (type.IsDataClass)
        {
            anchorType = type.ContainingType!;
        }

        foreach (var @interface in anchorType.DirectInterfaces)
        {
            if (@interface.DataClass is null) continue;

            writer.Write("private ");
            writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(".");
            writer.Write(InterfaceDataClassName);
            writer.Write(" ");
            EmitFakeDataFieldName(@interface, writer);
            writer.Write(";");
            writer.WriteLine();
        }

        foreach (var @interface in anchorType.DirectInterfaces)
        {
            if (@interface.DataClass is null) continue;

            writer.Write("private ");
            writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(".");
            writer.Write(InterfaceDataClassName);
            writer.Write(" Construct");
            writer.Write(@interface.RoslynSymbol.Name.WithoutInterfaceI());
            EmitMethodParameterList(
                @interface.DataClass.ConstructorIfDataClass!.RoslynSymbol,
                type.RoslynSymbol,
                writer,
                context);
            writer.WriteLine();
            writer.WriteBracedSectionStart();

            writer.Write("if (");
            if (type.IsDataClass)
            {
                writer.Write("((");
                writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
                writer.Write(")");
                writer.Write(nameof(InterfaceData<object>.This));
                writer.Write(").");
                writer.Write(GeneratedInterfaceDataPropertyName);
            }
            else
            {
                EmitRealDataFieldName(@interface, writer);
            }
            writer.Write(".");
            writer.Write(nameof(InterfaceData.__IsConstructed));
            writer.Write(") return null!;");
            writer.WriteLine();

            if (!type.IsDataClass)
            {
                foreach (var ancestorInterface in @interface.SelfAndInterfaces)
                {
                    if (ancestorInterface.DataClass is null) continue;

                    EmitRealDataFieldName(ancestorInterface, writer);
                    writer.Write(".");
                    writer.Write(nameof(InterfaceData<object>.__This));
                    writer.Write(" = this;");
                    writer.WriteLine();
                }
            }

            // @todo: remove string interpolations if needed for perf

            // @todo: create delegate and invoke that instead
            writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
            writer.Write(".");
            writer.Write(InterfaceDataClassName);
            writer.Write(".");
            writer.Write(GeneratedConstructorPropertyName);
            writer.Write(".Invoke(");
            if (type.IsDataClass)
            {
                writer.Write("((");
                writer.Write(@interface.RoslynSymbol.ToFullyQualifiedDisplayString());
                writer.Write(")");
                writer.Write(nameof(InterfaceData<object>.This));
                writer.Write(").");
                writer.Write(GeneratedInterfaceDataPropertyName);
            }
            else
            {
                EmitRealDataFieldName(@interface, writer);
            }
            writer.Write(", new object?[] { ");
            EmitUnparenthesizedArgumentListFromParameters(
                @interface.DataClass.ConstructorIfDataClass.RoslynSymbol.Parameters,
                writer);
            writer.Write(" });");
            writer.WriteLine();

            writer.WriteLine($"return null!;");

            writer.WriteBracedSectionEnd();
        }
    }

