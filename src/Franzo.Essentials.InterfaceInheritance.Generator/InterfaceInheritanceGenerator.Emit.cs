    private static void EmitUnparenthesizedParameterList(
        ImmutableArray<IParameterSymbol> parameters,
        INamedTypeSymbol withinType,
        IndentedTextWriter writer,
        Context context)
    {
        foreach ((var i, var parameter) in parameters.IndicesAndItems())
        {
            EmitAttributes(parameter, withinType, writer, context, false);
            writer.Write(parameter.Type.ToFullyQualifiedDisplayString());
            writer.Write(" ");
            parameter.WriteVerbatimizedName(writer);

            if (parameter.HasExplicitDefaultValue)
            {
                writer.Write("= ");
                EmitConstant(parameter.ExplicitDefaultValue, parameter.Type, writer);
            }

            if (i != parameters.Length - 1)
            {
                writer.Write(", ");
            }
        }
    }

    private static void EmitUnparenthesizedArgumentListFromMethodParameters(
        IMethodSymbol method,
        IndentedTextWriter writer)
    {
        EmitUnparenthesizedArgumentListFromParameters(method.Parameters, writer);
    }

    private static void EmitUnparenthesizedArgumentListFromParameters(
        ImmutableArray<IParameterSymbol> parameters,
        IndentedTextWriter writer)
    {
        foreach ((var i, var parameter) in parameters.IndicesAndItems())
        {
            var refKindModifier = parameter.RefKind.ToCSharpString();
            if (refKindModifier is not null)
            {
                writer.Write(refKindModifier);
                writer.Write(" ");
            }

            parameter.WriteVerbatimizedName(writer);

            if (i != parameters.Length - 1)
            {
                writer.Write(", ");
            }
        }
    }

    private static void EmitFeatureInheritance(
        InternalFeatureSymbol feature,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        IndentedTextWriter writer,
        Context context)
    {
        EmitAttributes(feature.RoslynSymbol, withinType, writer, context, true);
        EmitMainModifiers(feature.RoslynSymbol, writer, false);
        if (feature.HasOverrideAttribute)
        {
            writer.Write(" override");
        }

        writer.Write(" ");

        switch (feature)
        {
            case InternalPropertySymbol property:
                EmitPropertyInheritanceCore(property, declaringType, withinType, writer, context);
                break;
            case InternalFieldSymbol field:
                EmitFieldInheritanceCore(field, declaringType, withinType, writer, context);
                break;
            case InternalEventSymbol @event:
                EmitEventInheritanceCore(@event, declaringType, withinType, writer, context);
                break;
            case InternalMethodSymbol method:
                EmitMethodInheritanceCore(method, declaringType, withinType, writer, context);
                break;
        }
    }

    private static void EmitFeatureLocationExpression(
        InternalFeatureSymbol feature,
        INamedTypeSymbol declaringType,
        IndentedTextWriter writer)
    {
        if (feature.RoslynSymbol.IsStatic)
        {
            writer.Write(declaringType.ToFullyQualifiedDisplayString());
        }
        else
        {
            writer.Write("((");
            writer.Write(declaringType.ToFullyQualifiedDisplayString());
            writer.Write(")this)");
        }
    }

    private static void EmitPropertyInheritanceCore(
        InternalPropertySymbol property,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        IndentedTextWriter writer,
        Context context)
    {
        writer.Write(property.RoslynSymbol.Type.ToFullyQualifiedDisplayString());
        writer.Write(" ");
        writer.Write(property.RoslynSymbol.Name);
        if (property.RoslynSymbol.IsIndexer)
        {
            writer.Write("[");
            EmitUnparenthesizedParameterList(
                property.RoslynSymbol.Parameters,
                withinType,
                writer,
                context);
            writer.Write("]");
        }
        writer.WriteLine();

        writer.WriteBracedSectionStart();

        if (property.RoslynSymbol.GetMethod is not null)
        {
            EmitAttributes(property.RoslynSymbol.GetMethod, withinType, writer, context, false);
            writer.Write("get => ");
            EmitAccessExpressionForPropertyInheritance(property, declaringType, writer);
            writer.Write(";");
            writer.WriteLine();
        }

        if (property.RoslynSymbol.SetMethod is not null
            && property.RoslynSymbol.SetMethod.DeclaredAccessibility is not Accessibility.Private)
        {
            EmitAttributes(property.RoslynSymbol.SetMethod, withinType, writer, context, false);
            EmitAccessibilityModifiersForPropertyAccessor(
                property.RoslynSymbol.SetMethod,
                property.RoslynSymbol,
                writer);
            writer.Write("set => ");
            EmitAccessExpressionForPropertyInheritance(property, declaringType, writer);
            writer.Write(" = value;");
            writer.WriteLine();
        }

        writer.WriteBracedSectionEnd();
    }

    private static void EmitAccessExpressionForPropertyInheritance(
        InternalPropertySymbol property,
        INamedTypeSymbol declaringType,
        IndentedTextWriter writer)
    {
        // @todo: should generate proxies for interface data events
        EmitFeatureLocationExpression(property, declaringType, writer);

        if (property.RoslynSymbol.IsIndexer)
        {
            writer.Write("[");
            EmitUnparenthesizedArgumentListFromParameters(property.RoslynSymbol.Parameters, writer);
            writer.Write("]");
        }
        else
        {
            writer.Write(".");
            writer.Write(property.RoslynSymbol.Name);
        }
    }

    private static void EmitAccessibilityModifiersForPropertyAccessor(
        IMethodSymbol accessor,
        IPropertySymbol property,
        IndentedTextWriter writer)
    {
        if (accessor.DeclaredAccessibility == property.DeclaredAccessibility)
        {
            return;
        }

        if (accessor.DeclaredAccessibility is Accessibility.Protected)
        {
            writer.Write("protected");
        }
        else if (accessor.DeclaredAccessibility is Accessibility.Internal)
        {
            writer.Write("internal");
        }
        else if (accessor.DeclaredAccessibility is Accessibility.Private)
        {
            writer.Write("private");
        }
        else if (accessor.DeclaredAccessibility is Accessibility.ProtectedOrInternal)
        {
            writer.Write("protected internal");
        }
        else if (accessor.DeclaredAccessibility is Accessibility.ProtectedAndInternal)
        {
            writer.Write("private protected");
        }
    }

    private static void EmitFieldInheritanceCore(
        InternalFieldSymbol field,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        IndentedTextWriter writer,
        Context context)
    {
        if (field.RoslynSymbol.IsReadOnly)
        {
            writer.Write("readonly ");
        }
        else if (field.RoslynSymbol.IsConst)
        {
            writer.Write("const ");
        }
        writer.Write(field.RoslynSymbol.Type.ToFullyQualifiedDisplayString());
        writer.Write(" ");
        writer.Write(field.RoslynSymbol.Name);
        writer.Write(" = ");
        EmitFeatureLocationExpression(field, declaringType, writer);
        writer.Write(".");
        writer.Write(field.RoslynSymbol.Name);
        writer.Write(";");
        writer.WriteLine();
    }

    private static void EmitEventInheritanceCore(
        InternalEventSymbol @event,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        IndentedTextWriter writer,
        Context context)
    {
        writer.Write("event ");
        writer.Write(@event.RoslynSymbol.Type.ToFullyQualifiedDisplayString());
        writer.Write(" ");
        writer.Write(@event.RoslynSymbol.Name);
        writer.WriteLine();

        writer.WriteBracedSectionStart();

        if (@event.RoslynSymbol.AddMethod is not null)
        {
            EmitAttributes(@event.RoslynSymbol.AddMethod, withinType, writer, context, false);
            writer.Write("add => ");
            EmitFeatureLocationExpression(@event, declaringType, writer);
            writer.Write(".");
            writer.Write(@event.RoslynSymbol.Name);
            writer.Write(" += value;");
            writer.WriteLine();
        }

        if (@event.RoslynSymbol.RemoveMethod is not null)
        {
            EmitAttributes(@event.RoslynSymbol.RemoveMethod, withinType, writer, context, false);
            writer.Write("remove => ");
            EmitFeatureLocationExpression(@event, declaringType, writer);
            writer.Write(".");
            writer.Write(@event.RoslynSymbol.Name);
            writer.Write(" -= value;");
            writer.WriteLine();
        }

        writer.WriteBracedSectionEnd();
    }

    private static void EmitMethodInheritanceCore(
        InternalMethodSymbol method,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        IndentedTextWriter writer,
        Context context)
    {
        writer.Write(method.RoslynSymbol.ReturnType.ToFullyQualifiedDisplayString());
        writer.Write(" ");
        writer.Write(method.RoslynSymbol.Name);
        EmitTypeParameterList(method.RoslynSymbol.TypeParameters, writer);
        EmitMethodParameterList(method.RoslynSymbol, withinType, writer, context);
        writer.WriteLine();

        foreach (var typeParameter in method.RoslynSymbol.TypeParameters)
        {
            if (!typeParameter.HasConstraints()) continue;

            // @todo: EnumerableExtensions.WithLastFlag

            writer.Indent++;

            writer.Write("where ");
            writer.Write(typeParameter.Name);
            writer.Write(" : ");

            var constraintStrings = EnumerateConstraintStrings(typeParameter).ToArray();
            foreach ((var i, var constraintString) in constraintStrings.IndicesAndItems())
            {
                writer.Write(constraintString);
                if (i != constraintStrings.Length - 1)
                {
                    writer.Write(", ");
                }
            }

            writer.WriteLine();
            writer.Indent--;
        }

        writer.WriteBracedSectionStart();

        if (!method.RoslynSymbol.ReturnsVoid)
        {
            writer.Write("return ");
        }
        EmitFeatureLocationExpression(method, declaringType, writer);
        writer.Write(".");
        writer.Write(method.RoslynSymbol.Name);
        EmitTypeParameterList(method.RoslynSymbol.TypeParameters, writer);
        writer.Write("(");
        EmitUnparenthesizedArgumentListFromMethodParameters(method.RoslynSymbol, writer);
        writer.Write(");");
        writer.WriteLine();

        writer.WriteBracedSectionEnd();
    }

    private static IEnumerable<string> EnumerateConstraintStrings(
        ITypeParameterSymbol typeParameter)
    {
        if (typeParameter.HasReferenceTypeConstraint)
        {
            if (typeParameter.ReferenceTypeConstraintNullableAnnotation is NullableAnnotation.Annotated)
            {
                yield return "class?";
            }
            else
            {
                yield return "class";
            }
        }

        if (typeParameter.HasValueTypeConstraint)
        {
            yield return "struct";
        }

        if (typeParameter.HasUnmanagedTypeConstraint)
        {
            yield return "unmanaged";
        }

        if (typeParameter.HasNotNullConstraint)
        {
            yield return "notnull";
        }

        foreach (var constraintType in typeParameter.ConstraintTypes)
        {
            yield return constraintType.ToFullyQualifiedDisplayString();
        }

        if (typeParameter.HasConstructorConstraint)
        {
            yield return "new()";
        }
    }

    private static void EmitTypeParameterList(
        ImmutableArray<ITypeParameterSymbol> typeParameters,
        IndentedTextWriter writer)
    {
        if (typeParameters.Length == 0)
        {
            return;
        }

        writer.Write("<");
        foreach ((var i, var typeParameter) in typeParameters.IndicesAndItems())
        {
            writer.Write(typeParameter.Name);
            if (i != typeParameters.Length - 1)
            {
                writer.Write(", ");
            }
        }
        writer.Write(">");
    }

    private static void EmitDataClassPropertyProxy(
        IPropertySymbol property,
        INamedTypeSymbol withinType,
        IndentedTextWriter writer,
        Context context)
    {
        EmitAttributes(property, withinType, writer, context, true);
        EmitMainModifiers(property, writer, !property.IsVirtual);
        writer.Write(" ");
        writer.Write(property.Type.ToFullyQualifiedDisplayString());
        writer.Write(" ");
        writer.Write(property.Name);
        writer.WriteLine();

        writer.WriteBracedSectionStart();

        // @todo: if the property (or its setter) is marked private or protected, we need to use UnsafeAccessor

        if (property.GetMethod is not null)
        {
            EmitAttributes(property.GetMethod, withinType, writer, context, false);
            writer.Write("get => ");
            writer.Write(GeneratedInterfaceDataPropertyName);
            writer.Write(".");
            writer.Write(property.Name);
            writer.Write(";");
            writer.WriteLine();
        }

        if (property.SetMethod is not null)
        {
            EmitAttributes(property.SetMethod, withinType, writer, context, false);
            EmitAccessibilityModifiersForPropertyAccessor(property.SetMethod, property, writer);
            writer.Write("set => ");
            writer.Write(GeneratedInterfaceDataPropertyName);
            writer.Write(".");
            writer.Write(property.Name);
            writer.Write(" = value;");
            writer.WriteLine();
        }

        writer.WriteBracedSectionEnd();
    }
