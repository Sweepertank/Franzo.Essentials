            {
                writer.Write(", ");
                EmitUnparenthesizedArgumentListFromParameters(method.RoslynSymbol.Parameters, writer);
            }
            writer.Write(")");
        }
        else
        {
            EmitFeatureLocationExpression(method, declaringType, writer);
