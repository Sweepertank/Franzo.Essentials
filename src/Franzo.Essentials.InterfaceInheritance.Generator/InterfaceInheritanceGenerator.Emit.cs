    private static void EmitUnparenthesizedArgumentListFromParameters(
        ImmutableArray<IParameterSymbol> parameters,
        TypeEmissionContext cxt)
    {
        foreach ((var parameter, var last) in parameters.WithLastFlag())
        {
            var refKindModifier = parameter.RefKind.ToCSharpString();
            if (refKindModifier is not null)
            {
                cxt.Writer.Write(refKindModifier);
                cxt.Writer.Write(" ");
            }

            parameter.WriteVerbatimizedName(cxt.Writer);

            if (!last)
            {
                cxt.Writer.Write(", ");
            }
        }
    }

    private static void EmitFeatureInheritance(
        InternalFeatureSymbol feature,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt)
    {
        EmitAttributes(feature.RoslynSymbol, withinType, cxt, true);
        if (feature is not InternalFieldSymbol
            && feature.RoslynSymbol.DeclaredAccessibility is Accessibility.Public
            && !feature.HasOverrideAttribute)
        {
            cxt.Writer.Write("[");
#pragma warning disable CS0618
            cxt.Writer.Write(typeof(InterfaceInheritanceDevirtualizeAttribute).GloballyQualifiedName());
#pragma warning restore CS0618
            cxt.Writer.Write("]");
            cxt.Writer.WriteLine();
        }
        EmitMainModifiers(feature.RoslynSymbol, cxt);
        if (feature.HasOverrideAttribute)
        {
            cxt.Writer.Write(" override");
        }

        cxt.Writer.Write(" ");

        switch (feature)
        {
            case InternalPropertySymbol property:
                EmitPropertyInheritanceCore(property, declaringType, withinType, cxt);
                break;
            case InternalFieldSymbol field:
                EmitFieldInheritanceCore(field, declaringType, withinType, cxt);
                break;
            case InternalEventSymbol @event:
                EmitEventInheritanceCore(@event, declaringType, withinType, cxt);
                break;
            case InternalMethodSymbol method:
                EmitMethodInheritanceCore(method, declaringType, withinType, cxt);
                break;
        }
    }

    private static void EmitFeatureLocationExpression(
        InternalFeatureSymbol feature,
        INamedTypeSymbol declaringType,
        TypeEmissionContext cxt)
    {
        if (feature.RoslynSymbol.IsStatic)
        {
            cxt.Writer.Write(declaringType.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        }
        else
        {
            cxt.Writer.Write("((");
            cxt.Writer.Write(declaringType.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(")this)");
        }
    }

    private static void EmitPropertyInheritanceCore(
        InternalPropertySymbol property,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt)
    {
        cxt.Writer.Write(property.RoslynSymbol.Type.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write(" ");
        if (property.RoslynSymbol.IsIndexer)
        {
            cxt.Writer.Write("this[");
            EmitUnparenthesizedParameterList(
                property.RoslynSymbol.Parameters,
                withinType,
                cxt);
            cxt.Writer.Write("]");
        }
        else
        {
            cxt.Writer.Write(property.RoslynSymbol.Name);
        }
        cxt.Writer.WriteLine();

        cxt.Writer.WriteBracedSectionStart();

        if (property.RoslynSymbol.GetMethod is not null)
        {
            EmitAttributes(property.RoslynSymbol.GetMethod, withinType, cxt, false);
            cxt.Writer.Write("get");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            if (property.RoslynSymbol.IsProtectedOrProtectedAndOrInternal()
                && !property.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
            {
                EmitUnsafeGetterAccessorForProperty(property.RoslynSymbol, declaringType, cxt);
            }

            cxt.Writer.Write("return ");

            if (property.RoslynSymbol.IsProtectedOrProtectedAndOrInternal())
            {
                if (property.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
                {
                    EmitUnsafeAccessorClassQualification(
                        property.RoslynSymbol.GetMethod,
                        declaringType,
                        cxt);
                }

                EmitUnsafeGetterInvocationForProperty(property.RoslynSymbol, cxt, "this");
            }
            else
            {
                EmitAccessExpressionForPropertyInheritance(property, declaringType, cxt);
            }

            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionEnd();
        }

        if (property.RoslynSymbol.SetMethod is not null
            && property.RoslynSymbol.SetMethod.DeclaredAccessibility is not Accessibility.Private)
        {
            EmitAttributes(property.RoslynSymbol.SetMethod, withinType, cxt, false);
            EmitAccessibilityModifiersForPropertyAccessor(
                property.RoslynSymbol.SetMethod,
                property.RoslynSymbol,
                cxt);
            cxt.Writer.Write("set");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            if (property.RoslynSymbol.SetMethod.IsProtectedOrProtectedAndOrInternal())
            {
                if (property.RoslynSymbol.SetMethod.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
                {
                    EmitUnsafeAccessorClassQualification(
                        property.RoslynSymbol.SetMethod,
                        declaringType,
                        cxt);
                }
                else
                {
                    EmitUnsafeSetterAccessorForProperty(property.RoslynSymbol, declaringType, cxt);
                }

                EmitUnsafeSetterInvocationForProperty(property.RoslynSymbol, cxt, "this");
            }
            else
            {
                EmitAccessExpressionForPropertyInheritance(property, declaringType, cxt);
                cxt.Writer.Write(" = value");
            }

            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionEnd();
        }

        cxt.Writer.WriteBracedSectionEnd();

        if (property.RoslynSymbol.GetMethod is not null
            && property.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
        {
            EmitUnsafeGetterAccessorClassForProperty(property.RoslynSymbol, declaringType, cxt);
        }

        if (property.RoslynSymbol.SetMethod is not null
            && property.RoslynSymbol.SetMethod.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
        {
            EmitUnsafeSetterAccessorClassForProperty(property.RoslynSymbol, declaringType, cxt);
        }
    }

    private static void EmitAccessExpressionForPropertyInheritance(
        InternalPropertySymbol property,
        INamedTypeSymbol declaringType,
        TypeEmissionContext cxt)
    {
        EmitFeatureLocationExpression(property, declaringType, cxt);

        if (property.RoslynSymbol.IsIndexer)
        {
            cxt.Writer.Write("[");
            EmitUnparenthesizedArgumentListFromParameters(property.RoslynSymbol.Parameters, cxt);
            cxt.Writer.Write("]");
        }
        else
        {
            cxt.Writer.Write(".");
            cxt.Writer.Write(property.RoslynSymbol.Name);
        }
    }

    private static void EmitAccessibilityModifiersForPropertyAccessor(
        IMethodSymbol accessor,
        IPropertySymbol property,
        TypeEmissionContext cxt)
    {
        if (accessor.DeclaredAccessibility == property.DeclaredAccessibility)
        {
            return;
        }

        if (accessor.DeclaredAccessibility is Accessibility.Protected)
        {
            cxt.Writer.Write("protected");
        }
        else if (accessor.DeclaredAccessibility is Accessibility.Internal)
        {
            cxt.Writer.Write("internal");
        }
        else if (accessor.DeclaredAccessibility is Accessibility.Private)
        {
            cxt.Writer.Write("private");
        }
        else if (accessor.DeclaredAccessibility is Accessibility.ProtectedOrInternal)
        {
            cxt.Writer.Write("protected internal");
        }
        else if (accessor.DeclaredAccessibility is Accessibility.ProtectedAndInternal)
        {
            cxt.Writer.Write("private protected");
        }

        cxt.Writer.Write(" ");
    }

    private static void EmitFieldInheritanceCore(
        InternalFieldSymbol field,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt)
    {
        if (field.RoslynSymbol.IsReadOnly)
        {
            cxt.Writer.Write("readonly ");
        }
        else if (field.RoslynSymbol.IsConst)
        {
            cxt.Writer.Write("const ");
        }
        cxt.Writer.Write(field.RoslynSymbol.Type.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write(" ");
        cxt.Writer.Write(field.RoslynSymbol.Name);
        cxt.Writer.Write(" = ");

        if (field.RoslynSymbol.IsProtectedOrProtectedAndOrInternal())
        {
            if (field.RoslynSymbol.IsConst)
            {
                EmitConstant(field.RoslynSymbol.ConstantValue, field.RoslynSymbol.Type, cxt);
            }
            else
            {
                if (field.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
                {
                    EmitUnsafeAccessorClassQualification(field.RoslynSymbol, declaringType, cxt);
                }

                field.RoslynSymbol.WriteUnsafeAccessorName(cxt.Writer);
                cxt.Writer.Write("(null!)");
            }
        }
        else
        {
            EmitFeatureLocationExpression(field, declaringType, cxt);
            cxt.Writer.Write(".");
            cxt.Writer.Write(field.RoslynSymbol.Name);
        }

        cxt.Writer.Write(";");
        cxt.Writer.WriteLine();

        if (field.RoslynSymbol.IsProtectedOrProtectedAndOrInternal() && !field.RoslynSymbol.IsConst)
        {
            if (field.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
            {
                EmitUnsafeAccessorClassForField(field.RoslynSymbol, declaringType, cxt);
            }
            else
            {
                EmitUnsafeAccessorForField(field.RoslynSymbol, declaringType, cxt);
            }
        }
    }

    private static void EmitEventInheritanceCore(
        InternalEventSymbol @event,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt)
    {
        cxt.Writer.Write("event ");
        cxt.Writer.Write(@event.RoslynSymbol.Type.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write(" ");
        cxt.Writer.Write(@event.RoslynSymbol.Name);
        cxt.Writer.WriteLine();

        cxt.Writer.WriteBracedSectionStart();

        if (@event.RoslynSymbol.AddMethod is not null)
        {
            EmitAttributes(@event.RoslynSymbol.AddMethod, withinType, cxt, false);
            cxt.Writer.Write("add");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            if (@event.RoslynSymbol.IsProtectedOrProtectedAndOrInternal())
            {
                if (@event.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
                {
                    EmitUnsafeAccessorClassQualification(
                        @event.RoslynSymbol.AddMethod,
                        declaringType,
                        cxt);
                }
                else
                {
                    EmitUnsafeAddMethodAccessorForEvent(@event.RoslynSymbol, declaringType, cxt);
                }

                EmitUnsafeAddMethodInvocationForEvent(@event.RoslynSymbol, cxt, "this");
            }
            else
            {
                EmitFeatureLocationExpression(@event, declaringType, cxt);
                cxt.Writer.Write(".");
                cxt.Writer.Write(@event.RoslynSymbol.Name);
                cxt.Writer.Write(" += value");
            }

            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionEnd();
        }

        if (@event.RoslynSymbol.RemoveMethod is not null)
        {
            EmitAttributes(@event.RoslynSymbol.RemoveMethod, withinType, cxt, false);
            cxt.Writer.Write("remove");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            if (@event.RoslynSymbol.IsProtectedOrProtectedAndOrInternal())
            {
                if (@event.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
                {
                    EmitUnsafeAccessorClassQualification(
                        @event.RoslynSymbol.RemoveMethod,
                        declaringType,
                        cxt);
                }
                else
                {
                    EmitUnsafeRemoveMethodAccessorForEvent(@event.RoslynSymbol, declaringType, cxt);
                }

                EmitUnsafeRemoveMethodInvocationForEvent(@event.RoslynSymbol, cxt, "this");
            }
            else
            {
                EmitFeatureLocationExpression(@event, declaringType, cxt);
                cxt.Writer.Write(".");
                cxt.Writer.Write(@event.RoslynSymbol.Name);
                cxt.Writer.Write(" -= value");
            }

            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionEnd();
        }

        cxt.Writer.WriteBracedSectionEnd();

        if (@event.RoslynSymbol.AddMethod is not null
            && @event.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
        {
            EmitUnsafeAddMethodAccessorClassForEvent(@event.RoslynSymbol, declaringType, cxt);
        }

        if (@event.RoslynSymbol.RemoveMethod is not null
            && @event.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
        {
            EmitUnsafeRemoveMethodAccessorClassForEvent(@event.RoslynSymbol, declaringType, cxt);
        }
    }

    private static void EmitMethodInheritanceCore(
        InternalMethodSymbol method,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt)
    {
        EmitMethodCoreSignature(method.RoslynSymbol, declaringType, withinType, cxt);
        cxt.Writer.WriteLine();

        cxt.Writer.WriteBracedSectionStart();

        if (method.RoslynSymbol.IsProtectedOrProtectedAndOrInternal()
            && !method.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
        {
            EmitUnsafeAccessorForMethod(method.RoslynSymbol, declaringType, cxt);
        }

        if (!method.RoslynSymbol.ReturnsVoid)
        {
            cxt.Writer.Write("return ");
        }

        if (method.RoslynSymbol.IsProtectedOrProtectedAndOrInternal())
        {
            if (method.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
            {
                EmitUnsafeAccessorClassQualification(method.RoslynSymbol, declaringType, cxt);
            }

            cxt.Writer.Write(method.RoslynSymbol.Name);
            //method.RoslynSymbol.WriteUnsafeAccessorName(writer);
            EmitTypeParameterList(method.RoslynSymbol.TypeParameters, cxt);
            cxt.Writer.Write("(");
            if (method.RoslynSymbol.IsStatic)
            {
                cxt.Writer.Write("null!");
            }
            else
            {
                cxt.Writer.Write("this");
            }

            if (method.RoslynSymbol.Parameters.Length > 0)
            {
                cxt.Writer.Write(", ");
                EmitUnparenthesizedArgumentListFromParameters(method.RoslynSymbol.Parameters, cxt);
            }
            cxt.Writer.Write(")");
        }
        else
        {
            EmitFeatureLocationExpression(method, declaringType, cxt);
            cxt.Writer.Write(".");
            cxt.Writer.Write(method.RoslynSymbol.Name);
            EmitTypeParameterList(method.RoslynSymbol.TypeParameters, cxt);
            cxt.Writer.Write("(");
            EmitUnparenthesizedArgumentListFromParameters(method.RoslynSymbol.Parameters, cxt);
            cxt.Writer.Write(")");
        }

        cxt.Writer.Write(";");
        cxt.Writer.WriteLine();

        cxt.Writer.WriteBracedSectionEnd();

        if (method.RoslynSymbol.IsProtectedOrProtectedAndOrInternalAndOriginalDefinitionIsGenericTypeContained())
        {
            EmitUnsafeAccessorClassForMethod(method.RoslynSymbol, declaringType, cxt);
        }
    }

    private static void EmitMethodCoreSignature(
        IMethodSymbol method,
        INamedTypeSymbol declaringType,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt,
        bool isForUnsafeAccessor = false)
    {
        cxt.Writer.Write(method.ReturnType.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write(" ");
        /*if (isForUnsafeAccessor)
        {
            method.WriteUnsafeAccessorName(writer);
        }
        else
        {*/
        cxt.Writer.Write(method.Name);
        //}
        EmitTypeParameterList(method.TypeParameters, cxt);
        cxt.Writer.Write("(");
        if (isForUnsafeAccessor)
        {
            cxt.Writer.Write(declaringType.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(" c");
            if (method.Parameters.Length > 0)
            {
                cxt.Writer.Write(", ");
            }
        }
        EmitUnparenthesizedParameterList(
            method.Parameters,
            withinType,
            cxt,
            isForUnsafeAccessor: isForUnsafeAccessor);
        cxt.Writer.Write(")");
        EmitConstraintClausesWithLeadingNewLineIfAny(method.TypeParameters, cxt);
    }

    private static void EmitConstraintClausesWithLeadingNewLineIfAny(
        ImmutableArray<ITypeParameterSymbol> typeParameters,
        TypeEmissionContext cxt)
    {
        foreach (var typeParameter in typeParameters)
        {
            if (!typeParameter.HasConstraints()) continue;

            cxt.Writer.WriteLine();

            // @todo: EnumerableExtensions.WithLastFlag

            cxt.Writer.Indent++;

            cxt.Writer.Write("where ");
            cxt.Writer.Write(typeParameter.Name);
            cxt.Writer.Write(" : ");

            var constraintStrings = EnumerateConstraintStrings(typeParameter).ToArray();
            foreach ((var constraintString, var last) in constraintStrings.WithLastFlag())
            {
                cxt.Writer.Write(constraintString);
                if (!last)
                {
                    cxt.Writer.Write(", ");
                }
            }

            cxt.Writer.Indent--;
        }
    }

    private static IEnumerable<string> EnumerateConstraintStrings(
        ITypeParameterSymbol typeParameter)
    {
        if (typeParameter.HasReferenceTypeConstraint)
        {
            if (typeParameter.ReferenceTypeConstraintNullableAnnotation is NullableAnnotation.Annotated)
            {
                yield return "class?";
            }
            else
            {
                yield return "class";
            }
        }

        if (typeParameter.HasValueTypeConstraint && !typeParameter.HasUnmanagedTypeConstraint)
        {
            yield return "struct";
        }

        if (typeParameter.HasUnmanagedTypeConstraint)
        {
            yield return "unmanaged";
        }

        if (typeParameter.HasNotNullConstraint)
        {
            yield return "notnull";
        }

        foreach (var constraintType in typeParameter.ConstraintTypes)
        {
            yield return constraintType.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString();
        }

        if (typeParameter.HasConstructorConstraint)
        {
            yield return "new()";
        }
    }

    private static void EmitTypeParameterList(
        ImmutableArray<ITypeParameterSymbol> typeParameters,
        TypeEmissionContext cxt)
    {
        if (typeParameters.Length == 0)
        {
            return;
        }

        cxt.Writer.Write("<");
        foreach ((var typeParameter, var last) in typeParameters.WithLastFlag())
        {
            cxt.Writer.Write(typeParameter.Name);
            if (!last)
            {
                cxt.Writer.Write(", ");
            }
        }
        cxt.Writer.Write(">");
    }

    private static void EmitDataClassPropertyProxy(
        IPropertySymbol property,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt)
    {
        EmitAttributes(property, withinType, cxt, true);
        EmitMainModifiers(
            property,
            cxt,
            includeSealed: !property.IsVirtual && property.DeclaredAccessibility is not Accessibility.Private);
        cxt.Writer.Write(" ");
        cxt.Writer.Write(property.Type.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write(" ");
        cxt.Writer.Write(property.Name);
        cxt.Writer.WriteLine();

        cxt.Writer.WriteBracedSectionStart();

        if (property.GetMethod is not null)
        {
            EmitAttributes(property.GetMethod, withinType, cxt, false);
            cxt.Writer.Write("get");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            if (!property.IsPublicOrInternal())
            {
                EmitUnsafeGetterAccessorForProperty(property, property.ContainingType, cxt);
            }

            cxt.Writer.Write("return ");
            if (property.IsPublicOrInternal())
            {
                cxt.Writer.Write(GeneratedInterfaceDataPropertyName);
                cxt.Writer.Write(".");
                cxt.Writer.Write(property.Name);
            }
            else
            {
                EmitUnsafeGetterInvocationForProperty(
                    property,
                    cxt,
                    GeneratedInterfaceDataPropertyName);
            }

            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionEnd();
        }

        if (property.SetMethod is not null)
        {
            EmitAttributes(property.SetMethod, withinType, cxt, false);
            EmitAccessibilityModifiersForPropertyAccessor(property.SetMethod, property, cxt);
            cxt.Writer.Write("set");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            if (property.SetMethod.IsPublicOrInternal())
            {
                cxt.Writer.Write(GeneratedInterfaceDataPropertyName);
                cxt.Writer.Write(".");
                cxt.Writer.Write(property.Name);
                cxt.Writer.Write(" = value");
            }
            else
            {
                EmitUnsafeSetterAccessorForProperty(property, property.ContainingType, cxt);
                EmitUnsafeSetterInvocationForProperty(
                    property,
                    cxt,
                    GeneratedInterfaceDataPropertyName);
            }

            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionEnd();
        }

        cxt.Writer.WriteBracedSectionEnd();
    }

    private static void EmitDataClassEventProxy(
        IEventSymbol @event,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt)
    {
        EmitAttributes(@event, withinType, cxt, true);
        EmitMainModifiers(
            @event,
            cxt,
            includeSealed: !@event.IsVirtual && @event.DeclaredAccessibility is not Accessibility.Private);
        cxt.Writer.Write(" event ");
        cxt.Writer.Write(@event.Type.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
        cxt.Writer.Write(" ");
        cxt.Writer.Write(@event.Name);
        cxt.Writer.WriteLine();

        cxt.Writer.WriteBracedSectionStart();

        if (@event.AddMethod is not null)
        {
            EmitAttributes(@event.AddMethod, withinType, cxt, false);
            cxt.Writer.Write("add");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            if (@event.IsPublicOrInternal())
            {
                cxt.Writer.Write(GeneratedInterfaceDataPropertyName);
                cxt.Writer.Write(".");
                cxt.Writer.Write(@event.Name);
                cxt.Writer.Write(" += value");
            }
            else
            {
                EmitUnsafeAddMethodAccessorForEvent(@event, @event.ContainingType, cxt);
                EmitUnsafeAddMethodInvocationForEvent(
                    @event,
                    cxt,
                    GeneratedInterfaceDataPropertyName);
            }

            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionEnd();
        }

        if (@event.RemoveMethod is not null)
        {
            EmitAttributes(@event.RemoveMethod, withinType, cxt, false);
            cxt.Writer.Write("remove");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            if (@event.IsPublicOrInternal())
            {
                cxt.Writer.Write(GeneratedInterfaceDataPropertyName);
                cxt.Writer.Write(".");
                cxt.Writer.Write(@event.Name);
                cxt.Writer.Write(" -= value");
            }
            else
            {
                EmitUnsafeRemoveMethodAccessorForEvent(@event, @event.ContainingType, cxt);
                EmitUnsafeRemoveMethodInvocationForEvent(
                    @event,
                    cxt,
                    GeneratedInterfaceDataPropertyName);
            }

            cxt.Writer.Write(";");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionEnd();
        }

        cxt.Writer.WriteBracedSectionEnd();

        EmitEventInvokeMethod(@event, withinType, cxt, isProxy: true);
    }

    private static void EmitEventInvokeMethod(
        IEventSymbol @event,
        INamedTypeSymbol withinType,
        TypeEmissionContext cxt,
        bool isProxy)
    {
        if (@event.Type is INamedTypeSymbol { DelegateInvokeMethod: not null } eventType)
        {
            if (isProxy)
            {
                cxt.Writer.Write("private");
            }
            else
            {
                cxt.Writer.Write("public");
            }
            cxt.Writer.Write(" ");
            if (@event.IsStatic)
            {
                cxt.Writer.Write("static ");
            }
            cxt.Writer.Write(eventType.DelegateInvokeMethod.ReturnType.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
            cxt.Writer.Write(" ");
            cxt.Writer.Write(@event.Name);
            cxt.Writer.Write("_Invoke");
            EmitMethodParameterList(
                eventType.DelegateInvokeMethod,
                withinType,
                cxt);
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionStart();

            if (!eventType.DelegateInvokeMethod.ReturnsVoid)
            {
                cxt.Writer.Write("return ");
            }
            if (isProxy)
            {
                cxt.Writer.Write(GeneratedInterfaceDataPropertyName);
                cxt.Writer.Write(".");
                cxt.Writer.Write(@event.Name);
                cxt.Writer.Write("_");
            }
            else
            {
                cxt.Writer.Write(@event.Name);
                cxt.Writer.Write("?.");
            }
            cxt.Writer.Write("Invoke(");
            EmitUnparenthesizedArgumentListFromParameters(
                eventType.DelegateInvokeMethod.Parameters,
                cxt);
            cxt.Writer.Write(");");
            cxt.Writer.WriteLine();

            cxt.Writer.WriteBracedSectionEnd();
        }
    }

    private static void EmitUnsafeAccessorClassName(
        ISymbol symbol,
        INamedTypeSymbol currentType,
        TypeEmissionContext cxt)
    {
        if (currentType.ContainingType is null)
        {
            cxt.Writer.Write(symbol.ContainingType.OriginalDefinition.ToMangledFullyQualifiedWithoutGlobalNamespaceDisplayString());
            cxt.Writer.Write("_");
            cxt.Writer.Write(symbol.Name);
            if (symbol is IMethodSymbol method && !method.MethodKind.IsAccessor())
            {
                cxt.Writer.Write("_Arity");
                cxt.Writer.Write(method.TypeParameters.Length);
                cxt.Writer.Write("_StartParameters__");
                foreach ((var parameter, var last) in method.Parameters.WithLastFlag())
                {
                    cxt.Writer.Write(parameter.Type.ToMangledFullyQualifiedWithoutGlobalNamespaceDisplayString());
                    if (!last)
                    {
                        cxt.Writer.Write("_");
                    }
                }
                cxt.Writer.Write("__EndParameters");
            }
            cxt.Writer.Write("_");
            cxt.Writer.Write(currentType.OriginalDefinition.Name);
            cxt.Writer.Write("_Class");
        }
        else
        {
            cxt.Writer.Write(currentType.OriginalDefinition.Name);
        }
    }

    private static void EmitUnsafeAccessorClassQualification(
        ISymbol symbol,
        INamedTypeSymbol declaringType,
        TypeEmissionContext cxt)
    {
        foreach (var type in declaringType.SelfAndContainingTypes().Reverse())
        {
            EmitUnsafeAccessorClassName(symbol, type, cxt);
            if (type.TypeArguments.Length > 0)
            {
                cxt.Writer.Write("<");
                foreach ((var typeArgument, var last) in type.TypeArguments.WithLastFlag())
                {
                    cxt.Writer.Write(typeArgument.ToFullyQualifiedWithNullableReferenceTypeAnnotationsDisplayString());
                    if (!last)
                    {
                        cxt.Writer.Write(", ");
                    }
                }
                cxt.Writer.Write(">");
            }
            cxt.Writer.Write(".");
        }
    }

    private static void EmitUnsafeAccessorClassTopBun(
        ISymbol symbol,
        INamedTypeSymbol declaringType,
        TypeEmissionContext cxt)
    {
        for (var i = 0; i < cxt.TypeStack.Count; i++)
        {
            CloseType(cxt);
        }

        foreach (var type in declaringType.SelfAndContainingTypes().Reverse())
        {
            if (type.ContainingType is null)
            {
                cxt.Writer.Write("file");
            }
            else
            {
                cxt.Writer.Write("public");
            }
            cxt.Writer.Write(" static class ");
            EmitUnsafeAccessorClassName(symbol, type, cxt);
            if (type.TypeParameters.Length > 0)
            {
                cxt.Writer.Write("<");
                foreach ((var typeParameter, var last) in type.TypeParameters.WithLastFlag())
                {
                    cxt.Writer.Write(typeParameter.Name);
                    if (!last)
                    {
                        cxt.Writer.Write(", ");
                    }
                }
                cxt.Writer.Write(">");
            }
            EmitConstraintClausesWithLeadingNewLineIfAny(type.TypeParameters, cxt);
            cxt.Writer.WriteLine();
            cxt.Writer.WriteBracedSectionStart();
        }
    }

    private static void EmitUnsafeAccessorClassBottomBun(
        ISymbol symbol,
        INamedTypeSymbol declaringType,
        TypeEmissionContext cxt)
    {
        foreach (var type in declaringType.SelfAndContainingTypes().Reverse())
        {
            cxt.Writer.WriteBracedSectionEnd();
        }

        foreach (var type in cxt.TypeStack.Reverse())
        {
            OpenType(type, cxt);
        }
    }

