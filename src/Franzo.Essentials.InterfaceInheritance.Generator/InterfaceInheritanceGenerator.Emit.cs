    private static void EmitMethodParameterList(
        IMethodSymbol method,
        INamedTypeSymbol withinType,
        IndentedTextWriter writer,
        Context context)
    {
        writer.Write("(");
        EmitUnparenthesizedParameterList(method.Parameters, withinType, writer, context);
